var dds = parseInt(prompt("digite um número de 1 a 7 correspondendo aos dias da semana (1 para domingo,2 para segunda etc"));

switch (dds){
    case 1:
        alert("domingo")
        break
    case 2:
        alert("segunda")
        break
    case 3:
        alert("terça")
        break
    case 4:
        alert("quarta")
        break
    case 5:
        alert("quinta")
        break
    case 6:
        alert("sexta")
        break
    case 7:
        alert("sábado")
        break
    default:
        alert("O número deve ser entre 1 e 7")
        break

}